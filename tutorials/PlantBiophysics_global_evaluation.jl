begin
	function RMSE(obs,sim)
	    return sqrt(sum((obs .- sim).^2)/length(obs))
	end
	
	function EF(obs,sim)
	    SSres = sum((obs - sim).^2)
	    SStot = sum((obs .- mean(obs)).^2)
	    return 1-SSres/SStot
	end
	
